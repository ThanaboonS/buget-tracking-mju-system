package explorer.application.bean;


public class AddItemBean {
	
	private String idItem;
	private int idPeriod;
	private String titleItem;
	private double budgetItem;
	private String detail;
	private String startItem;
	private String endItem;
	private String dateBook;
	
	
	public String getDateBook() {
		return dateBook;
	}
	public void setDateBook(String dateBook) {
		this.dateBook = dateBook;
	}
	public String getStartItem() {
		return startItem;
	}
	public void setStartItem(String startItem) {
		this.startItem = startItem;
	}
	public String getEndItem() {
		return endItem;
	}
	public void setEndItem(String endItem) {
		this.endItem = endItem;
	}
	public String getIdItem() {
		return idItem;
	}
	public void setIdItem(String idItem) {
		this.idItem = idItem;
	}
	public int getIdPeriod() {
		return idPeriod;
	}
	public void setIdPeriod(int idPeriod) {
		this.idPeriod = idPeriod;
	}
	public String getTitleItem() {
		return titleItem;
	}
	public void setTitleItem(String titleItem) {
		this.titleItem = titleItem;
	}
	public double getBudgetItem() {
		return budgetItem;
	}
	public void setBudgetItem(double budgetItem) {
		this.budgetItem = budgetItem;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	
	

	

}
